from logging import exception

#===================================================================================================

class Journal:
    """
    Journal class.
    """

#---------------------------------------------------------------------------------------------------

    def __init__(self, name):
        """
        Init journal.

        Parameters
        ----------
        name : str
            Name.
        """

        self.__name = name

#---------------------------------------------------------------------------------------------------

    @property
    def name(self):
        """
        Get name of journal.

        Returns
        -------
        str
            Name of journal.
        """

        if self.__name == '':
            raise exception('Journal: empty name')

        return self.__name

#===================================================================================================

if __name__ == '__main__':
    pass

